#include <unistd.h>

void	ft_print_comb(void)
{
	int	u;
	int	d;
	int	c;

	u = 48;
	while (u <= 55)
	{
		d = u;
		while (++d <= 56)
		{
			c = d;
			while (++c <= 57)
			{
				write(1, &u, 1);
				write(1, &d, 1);
				write(1, &c, 1);
				if (u != 55 || d != 56 || c != 57)
					write(1, ", ", 2);
			}
		}
		u++;
	}
}